package day05;

public class C01_syso_number_and_String {

	public static void main(String[] args) {
		System.out.println("Java"+30+20); //Java3020
		System.out.println("Java"+(30+20)); //Java50
		System.out.println(20+30+"Java"); //50Java
		System.out.println(""+20+30+"Java"); //2030Java
		System.out.println("\'"); // '
		System.out.println("\""); // "
		System.out.println(627%10); // 7
		System.out.println((627/10)%10); //2   627/10 = 62
		System.out.println(627/10%10); //2   627/10 = 62
	}

}
