package day03;

public class ExtraExamples {

	public static void main(String[] args) {
		
		int num = 56;
		System.out.println(num-= 4); //52
		
		System.out.println(num+= 14); //66
		
		System.out.println(num--); //66
		
		System.out.println(num); //65
		
		System.out.println(num++); //65
		
		System.out.println(--num); //65
		
		System.out.println(num+= 24);//89
		
		System.out.println(--num); //88
		
		System.out.println(num++); //88
		
		System.out.println(num+= 5); //94
		
		
		
		
	}

}
